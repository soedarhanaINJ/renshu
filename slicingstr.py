#huruf pertama index dimulai dari 0
x = 'Hello World' #huruf 'H' itu indexnya dari 0 spasi juga dihitung sebagai karakter
print(x[2:5])
print(x[:7]) #pengambilan dari 0(H) sampai W
print(x[3:]) #pengambilan dari index 3 sampai semua nilai dalam variable
print(x[-4:-1]) #pengambilan negatid dari karakter paling kanan, tapi index dimulai dari 1